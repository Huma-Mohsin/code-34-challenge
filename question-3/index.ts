// //Question 102: Calculate and log the absolute difference between the number -5 and 5.

// Explain & TIP: The Math.abs() function returns the absolute value of a number, which is useful for finding the magnitude of a difference without regard to direction.

function findAbsoluteDifference(){
    let differene= Math.abs(-5 +5);//Math.abs() is a function typically used to calculate the absolute value of a number. The absolute value of a number is its distance from zero on the number line, regardless of its sign.
    //It's commonly used in various programming tasks where we need to ensure that a number is treated as positive, or where you need to disregard its sign so we take absolute value to make number positive
    return differene;

}
console.log("The difference b/w 2 values is =",findAbsoluteDifference());
